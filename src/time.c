#include "philo.h"

long long	get_time(void)
{
	struct timeval	tv;

	gettimeofday(&tv, NULL);
	return ((tv.tv_sec * 1000) + (tv.tv_usec / 1000));
}

void	ft_sleep(int time)
{
	long long	curr;

	curr = get_time();
	while (get_time() - curr < time)
		usleep(100);
}
